    private void btnRegister1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegister1ActionPerformed
        String date = ((JTextField) dtcDOB.getDateEditor().getUiComponent()).getText();

        if (!chkCertify.isSelected()) {
            Sutil.msg(this, "Please agree to our terms in order to continue.");
        } else {
            if ((!txtName.getText().trim().isEmpty()) && (!txtPOB.getText().trim().isEmpty()) && (!txtPass1.getText().trim().isEmpty()) && (!txtPass2.getText().trim().isEmpty()) && (!txtPOB.getText().trim().isEmpty()) && (rdoMale.isSelected() || rdoFemale.isSelected()) &&!date.isEmpty()) {
              if (!txtPass1.getText().trim().toString().equals(txtPass2.getText().trim().toString())) {
              Sutil.msg(this, "Password do not match");
              }else{
                  try {
                      executeRegister();
                      this.setVisible(false);
                      FrmMain main= new FrmMain();
                      main.setVisible(true);
                  } catch (SQLException ex) {
                      Logger.getLogger(FrmRegistration.class.getName()).log(Level.SEVERE, null, ex);
                  }
              }
                
        }else{
                Sutil.msg(this, "Some field is still empty.");
            }
        }
    }//GEN-LAST:event_btnRegister1ActionPerformed

