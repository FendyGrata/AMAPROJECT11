    private void executeRegister() throws SQLException {
        String insertSql = "INSERT INTO akuntansi.member "
                + "(nama,uid,gender,pob,dob,password)"
                + "VALUES (?,?,?,?,?,?);";
        PreparedStatement pstatement = conn.prepareStatement(insertSql);
        pstatement.setString(1, txtName.getText().trim());
        pstatement.setString(2, txtUID.getText().trim().toLowerCase());
        if (rdoMale.isSelected()) {
            pstatement.setString(3, "Male");
        } else if (rdoFemale.isSelected()) {
            pstatement.setString(3, "Female");
        }

        pstatement.setString(4, txtPOB.getText().trim());
        pstatement.setObject(5, dtcDOB.getDate());
        pstatement.setString(6, txtPass2.getText().trim());
        pstatement.executeUpdate();
        pstatement.close();
        Sutil.msg(this, "You have been registered. Now please login again.");
    }
